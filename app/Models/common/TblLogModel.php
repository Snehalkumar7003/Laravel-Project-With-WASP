<?php

namespace App\Models\common;

use App\Models\masters\MstUserModel;
use Illuminate\Database\Eloquent\Model;

/**
 * |--------------------------------------------------------------------------
 * | Audit Log Model
 * |--------------------------------------------------------------------------
 * |
 * | Table      : tbl_logs
 * | Primary Key: tbl_logs_id
 * |
 * |--------------------------------------------------------------------------
 */
class TblLogModel extends Model{
    /**
     * |--------------------------------------------------------------------------
     * | Table Name
     * |--------------------------------------------------------------------------
     */
    protected $table = 'tbl_logs';

    /**
     * |--------------------------------------------------------------------------
     * | Primary Key
     * |--------------------------------------------------------------------------
     */
    protected $primaryKey = 'tbl_logs_id';

        /**
         * |--------------------------------------------------------------------------
         * | Disable Laravel Timestamps
         * |--------------------------------------------------------------------------
         */
    public $timestamps = false;

    /**
     * |--------------------------------------------------------------------------
     * | Mass Assignable Fields
     * |--------------------------------------------------------------------------
     */
    protected $fillable = [
        'module',
        'module_id',
        'ip_address',
        'inserted_data',
        'previous_data',
        'mst_users_id',
        'action',
        'remarks'
    ];

    /**
     * |--------------------------------------------------------------------------
     * | Type Casting
     * |--------------------------------------------------------------------------
     */
    protected $casts = [
        'module_id'    => 'integer',
        'mst_users_id' => 'integer',
        'create_date'  => 'datetime'
    ];

    /**
     * |--------------------------------------------------------------------------
     * | User Relationship
     * |--------------------------------------------------------------------------
     */
    public function user(){
        return $this->belongsTo(
            MstUserModel::class,
            'mst_users_id',
            'mst_users_id'
        );
    }
}
