<?php

namespace App\Models\masters;

use Illuminate\Database\Eloquent\Model;

/**
 * |--------------------------------------------------------------------------
 * | Role Model
 * |--------------------------------------------------------------------------
 * |
 * | Table      : mst_roles
 * | Primary Key: mst_roles_id
 * |
 * |--------------------------------------------------------------------------
 */
class MstRoleModel extends Model
{
    /**
     * |--------------------------------------------------------------------------
     * | Table Name
     * |--------------------------------------------------------------------------
     */
    protected $table = 'mst_roles';

    /**
     * |--------------------------------------------------------------------------
     * | Primary Key
     * |--------------------------------------------------------------------------
     */
    protected $primaryKey = 'mst_roles_id';

    /**
     * |--------------------------------------------------------------------------
     * | Disable Laravel Timestamps
     * |--------------------------------------------------------------------------
     */
    public $timestamps = false;

    /**
     * |--------------------------------------------------------------------------
     * | Mass Assignable Fields
     * |--------------------------------------------------------------------------
     */
    protected $fillable = [
        'role_name',
        'is_active',
        'is_delete',
        'last_update'
    ];

    /**
     * |--------------------------------------------------------------------------
     * | Type Casting
     * |--------------------------------------------------------------------------
     */
    protected $casts = [
        'is_active'   => 'boolean',
        'is_delete'   => 'boolean',
        'create_date' => 'datetime',
        'last_update' => 'datetime'
    ];

    /**
     * |--------------------------------------------------------------------------
     * | Users Relationship
     * |--------------------------------------------------------------------------
     */
    public function users()
    {
        return $this->hasMany(
            MstUserModel::class,
            'mst_roles_id',
            'mst_roles_id'
        );
    }
}
