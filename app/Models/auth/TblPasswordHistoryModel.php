<?php

namespace App\Models\auth;

use App\Models\masters\MstUserModel;
use Illuminate\Database\Eloquent\Model;

/**
 * |--------------------------------------------------------------------------
 * | Password History model
 * |--------------------------------------------------------------------------
 * |
 * | Table      : tbl_password_history
 * | Primary Key: tbl_password_history_id
 * |
 * |--------------------------------------------------------------------------
 */
class TblPasswordHistoryModel extends Model
{
    /**
     * |--------------------------------------------------------------------------
     * | Table Name
     * |--------------------------------------------------------------------------
    */
    protected $table='tbl_password_history';

     /**
     * |--------------------------------------------------------------------------
     * | Primary Key
     * |--------------------------------------------------------------------------
     */
    protected $primaryKey='tbl_password_history_id';

    /**
     * |--------------------------------------------------------------------------
     * | Disable Laravel Timestamps
     * |--------------------------------------------------------------------------
     */
    public $timestamps=false;

    
    /**
     * |--------------------------------------------------------------------------
     * | Mass Assignable Fields
     * |--------------------------------------------------------------------------
     */

    /**
     * |--------------------------------------------------------------------------
     * | User Relationship
     * |--------------------------------------------------------------------------
     */
    protected $fillable=[
        'mst_users_id',
        'password'
    ];

    /**
     * |--------------------------------------------------------------------------
     * | User Relationship
     * |--------------------------------------------------------------------------
     */
    public function user(){
        return $this->belongsTo(
            MstUserModel::class,
            'mst_users_id',
            'mst_users_id'
        );
    }
}