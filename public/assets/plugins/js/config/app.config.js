/**
 * |--------------------------------------------------------------------------
 * | Application Configuration
 * |--------------------------------------------------------------------------
 * |
 * | Reads application configuration securely from meta tags.
 * | Compatible with strict OWASP CSP policies.
 * |
 * |--------------------------------------------------------------------------
 */

window.APP_CONFIG = {
    /*
    |--------------------------------------------------------------------------
    | Base URL
    |--------------------------------------------------------------------------
    */
    BASE_URL:document.querySelector('meta[name="app-base-url"]').content,
    /*
    |--------------------------------------------------------------------------
    | CSRF Configuration
    |--------------------------------------------------------------------------
    */
    CSRF: {
        /*
        |--------------------------------------------------------------------------
        | Token Name
        |--------------------------------------------------------------------------
        */
        NAME:document.querySelector('meta[name="csrf-token-name"]').content,

        /*
        |--------------------------------------------------------------------------
        | Token Hash
        |--------------------------------------------------------------------------
        */
        HASH:document.querySelector('meta[name="csrf-token-hash"]').content
    },

    /*
    |--------------------------------------------------------------------------
    | Reset CSRF Token
    |--------------------------------------------------------------------------
    */
    setCSRFHash: function (newHash) {
        /*
        |--------------------------------------------------------------------------
        | Update Runtime Value
        |--------------------------------------------------------------------------
        */
        this.CSRF.HASH = newHash;
        /*
        |--------------------------------------------------------------------------
        | Update Meta Tag
        |--------------------------------------------------------------------------
        */
        document.querySelector('meta[name="csrf-token-hash"]').setAttribute(
            'content',
            newHash
        );
    }
};